function reset_trackingbox(){
	if(document.getElementById("tracking_no").value!=''){
		document.getElementById("tracking_no").value='';
	}else {
		document.getElementById("tracking_no").value='';
	}
}

function reset_actkeybox(){
	if(document.getElementById("activationkey").value!=''){
		document.getElementById("activationkey").value='';
	}else {
		document.getElementById("activationkey").value='';
	}
}